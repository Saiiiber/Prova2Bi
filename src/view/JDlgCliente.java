    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed
        // TODO add your handling code here:
        
        if (alterar == false)
        {
        Util.habilitar(true,  jTxtTelefone, jTxtTelFixo, jTxtProfissao, jTxtPreferencias ,jTxtNacionalidade, jFmtCep, jCboEstCivil, jTxtEmail, jTxtEndereco, jFmtRg, jFmtCpf, jTxtNome, jCboSexo, jTxtApelido, jBtnCancelar, jBtnCancelar);
        Util.habilitar(false, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);
        incluir = false;
        alterar = true;
        } else {
            Util.msg("Selecione um registro para alterar");
        }
    }//GEN-LAST:event_jBtnAlterarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:

        if( Util.pergunta("Excluir", "Excluir?") == YES_NO_OPTION){
            VeccCliente cliente = viewBean();
            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.delete(cliente);
            Util.msg("Exclus√£o efetuada");
        }else{
            Util.msg("Exclus√£o cancelada");

        }
        Util.limp( jTxtTelefone, jTxtTelFixo, jTxtProfissao, jTxtPreferencias ,jTxtNacionalidade, jFmtCep, jCboEstCivil, jTxtEmail, jTxtEndereco, jFmtRg, jFmtCpf, jTxtNome, jCboSexo, jTxtApelido);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        
        
        if (incluir == true) {
            VeccCliente usuarios = viewBean();
            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.insert(usuarios);
            Util.habilitar(false, jTxtIdade, jTxtId, jFmtData, jTxtTelefone, jTxtTelFixo, jTxtProfissao, jTxtPreferencias ,jTxtNacionalidade, jFmtCep, jCboEstCivil, jTxtEmail, jTxtEndereco, jFmtRg, jFmtCpf, jTxtNome, jCboSexo, jTxtApelido, jBtnCancelar, jBtnCancelar);
            Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);
            Util.limp(jTxtEmail, jTxtEndereco, jFmtRg, jFmtCpf, jTxtNome);
        }else{
            VeccCliente cliente = viewBean();
            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.update(cliente);
            Util.habilitar(false, jTxtEmail, jTxtEndereco, jFmtRg, jFmtCpf, jTxtNome, jBtnCancelar, jBtnCancelar);
            Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);
            Util.limp(jTxtEmail, jTxtEndereco, jFmtRg, jFmtCpf, jTxtNome);

        }

    }//GEN-LAST:event_jBtnConfirmarActionPerformed

